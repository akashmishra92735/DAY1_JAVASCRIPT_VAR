var num=80;//"Declare a variable named num and assign it the value 80."
var num=20;//In this we can redeclare the variable.
console.log(num);//Print the value of the variable num to the console.

//(2)let
let a=50;//Declare a block-scoped variable named a and assign it the value 50.
let a=40;//In this we can not redeclare the variable.
console.log(a);////Print the value of the variable a to the console.

//(3)console
